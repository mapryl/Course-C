int		ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;
	unsigned char	s1_n;
	unsigned char	s2_n;

	i = 0;
	s1_n = s1[i];
	s2_n = s2[i];
	while (s1_n != '\0' && s2_n != '\0')
	{
		s1_n = s1[i];
		s2_n = s2[i];
		if (s1_n != s2_n)
			return (s1_n - s2_n);
		i++;
	}
	if ((s1_n == '\0' && s2_n != '\0') ||
			(s2_n == '\0' && s1_n != '\0'))
		return (s1_n - s2_n);
	return (0);
}
