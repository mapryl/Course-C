int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int count_1;

	i = 0;
	count_1 = 0;
	while (tab[i] != 0)
	{
		if ((*f)(tab[i]) == 1)
			count_1++;
		i++;
	}
	return (count_1);
}
