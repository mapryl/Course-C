char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	buf;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i -= 1;
	j = 0;
	buf = '0';
	while (j < i)
	{
		buf = str[j];
		str[j] = str[i];
		str[i] = buf;
		j++;
		i--;
	}
	return (str);
}
