void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	while (i < argc - 1)
	{
		i++;
		ft_putstr(argv[argc - i]);
		ft_putchar('\n');
	}
	return (0);
}
